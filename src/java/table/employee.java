package table;


public class employee {
    private String id;
    private String name;
   
    
    
    public employee(){}
    
    public void setId(String id){
        this.id = id;
    }
    public void setName(String name){
        this.name = name;
    }
    
    
    public String getId(){
        return this.id;
    }
    public String getName(){
        return this.name;
    }
    
    
}
